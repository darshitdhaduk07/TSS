package com.tss.oop;

public class Member {
    private int memberId;
    private String name;

    public Member(int memberId,String name)
    {
        this.memberId = memberId;
        this.name = name;
    }

    public int getMemberId()
    {
        return memberId;
    }

    public String toString()
    {
        return name;
    }

}
